    public static getWordToday(): string {
        const date = new Date();
        const seed = (date.getDate() + date.getMonth() * 32 + date.getFullYear() * 32 * 13);

        const rand = Math.abs(Math.sin(seed)) * 10001;
        const index = Math.floor((rand - Math.floor(rand))*WordList.allSolutions.length);

        return WordList.allSolutions[index];
    }
